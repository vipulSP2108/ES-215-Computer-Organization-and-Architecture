DATA SEGMENT
  STR DB 'VIPUL'
  LEN DB ($-STR) - 1
  FIND DB 'U'
  MSG_FOUND DB 'CHARACTER FOUND AT '
  MSG_NOTFOUND DB 'CHARACTER NOT FOUND $'
  RES DB 'POSITION',13,10,' $'
DATA ENDS

CODE SEGMENT
  ASSUME DS:DATA, CS:CODE

MAIN:
  MOV AX, @DATA
  MOV DS, AX

  MOV AL, FIND
  MOV DX, LEN
  MOV CX, 0

LOOP:
  CMP CX, DX
  JE NOTFOUND

  MOV BL, STR[CX]
  CMP BL, AL
  JE FOUND

  INC CX
  JMP LOOP

FOUND:
  LEA DX, MSG_FOUND
  MOV AH, 09H
  INT 21H 

  MOV AL, CL 
  ADD AL, '1'
  MOV RES+10, AL 
  
  LEA DX, RES
  MOV AH, 09H
  INT 21H 

  JMP END_PROG 

NOTFOUND:
  LEA DX, MSG_NOTFOUND
  MOV AH, 09H
  INT 21H

END_PROG:
  MOV AH,4CH
  INT 21H 

CODE ENDS
END MAIN
